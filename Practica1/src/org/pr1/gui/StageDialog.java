    private Font getCustomFont(float size){
        Font font = null;
        try {
            font = Font.createFont(Font.TRUETYPE_FONT, new File(this.getClass().getResource("/org/pr1/resources/atari.ttf").getPath())).deriveFont(size);
            GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
            ge.registerFont(font);
        } catch (IOException|FontFormatException e) {
            //font = new Font("Segoe UI Semibold", 0, Integer.parseInt((String.valueOf(size))));
        }
        return font;
    }
    
    private void createRowsAndCols(int rows, int cols){
        pnlStage.removeAll();
        GridBagConstraints gbc = new GridBagConstraints(); 
        for (int row = 0; row < rows; row++) {
            for (int col = 0; col < cols; col++) {
                gbc.anchor = GridBagConstraints.FIRST_LINE_START;
                gbc.gridx = col;
                gbc.gridy = row;
                CellPane cellPane = new CellPane();
                Border border = null;
                if (row < (rows - 1)) {
                    if (col < (cols - 1)) {
                        border = new MatteBorder(1, 1, 0, 0, Color.GRAY);
                    } else {
                        border = new MatteBorder(1, 1, 0, 1, Color.GRAY);
                    }
                } else {
                    if (col < (cols - 1)) {
                        border = new MatteBorder(1, 1, 1, 0, Color.GRAY);
                    } else {
                        border = new MatteBorder(1, 1, 1, 1, Color.GRAY);
                    }
                }
                cellPane.setBorder(border);
                if(row == (rows - 1) && col == (cols - 1)){
                    gbc.weightx = 0.5;
                    gbc.weighty = 0.5;
                }
                pnlStage.add(cellPane, gbc);
            }
        }
        pnlStage.revalidate();
        pnlStage.repaint();
    }
   
